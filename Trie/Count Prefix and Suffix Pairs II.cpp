struct Node{
    unordered_map<int , Node*> children;
    int times = 0;
};

class Trie {
private: 
    Node* root;

public:
    
    Trie() {
        root = new Node(); 
    }
    
    long long solve(vector<string> arr) {
        long long ans = 0;
        for(string s : arr) {
            Node * curr = root;
            for(int i = 0; i < s.size(); i++) {
                int val = (s[i] - 'a') * 128 + (s[s.size() - i - 1] - 'a');
                if (curr->children.find(val) == curr->children.end()) {
                    curr->children[val] = new Node();
                }
                curr = curr->children[val];
                ans += curr->times;
            }
            curr->times++;
        }
        return ans;
    }
};

long long countPrefixSuffixPairs(vector<string>& words) {
    Trie t;
    return t.solve(words);
}









