struct Node {
    Node* link[26];
    bool flag = false;

    // check that chr is present or not ! 
    bool checkKey (char ch){
        return link[ch-'a'] !=NULL;
    }

    // go to that chat !
    Node * get (char ch){
        return link[ch-'a'];
    }

    // creating new 
    void put (char ch, Node * node){
        link[ch-'a']= node ;
    }
    // setting end 
    void setEnd(){
        flag = true;
    }
    // check it is end or not 
    bool isEnd(){
        return flag;
    }
};

class WordDictionary {
    private : 

    Node* root ;

    public :

    WordDictionary(){
        root = new Node() ;
    }

    void addWord(string word) {
        Node * node = root ;
        for(int i=0;i<word.length();i++){
            if(!node->checkKey(word[i])){
                node->put(word[i], new Node());
            }
            node = node->get(word[i]);
        }
        node->setEnd();
    }

    bool search(string word) {
        Node* node = root ;
        return searchWord(word, 0, node);
    }

private:
    bool searchWord(const string& word, int index, Node* node) {
        if (index == word.size()) {
            return node->isEnd();
        }
        char ch = word[index];
        if (ch == '.') {
            // Check all possible children nodes
            for (int i = 0; i < 26; i++) {
                if (node->link[i] != NULL && searchWord(word, index + 1, node->link[i])) {
                    return true;
                }
            }
            return false;
        } else {
            // Normal character check
            if (!node->checkKey(ch)) {
                return false;
            }
            return searchWord(word, index + 1, node->get(ch));
        }
    }


};









