bool solve(int i,string s , vector<string>&arr , vector<int>&dp){
    if(i>=s.size())return true;
    if(dp[i]!=-1)return dp[i]==1;

    for(string a : arr){
       int n = a.size();
       if(i+n>s.size())continue;

       if(s.substr(i,n)==a && solve(i+n,s,arr,dp)){
        dp[i]=1;
        return true;
       }
    }
    dp[i]=0;
    return false;
}


