#include <iostream>
#include <vector>
#include <unordered_map>
using namespace std;

long long solve(const vector<int>& a, const vector<int>& b, int m) {
    unordered_map<int, int> remainder_count;
    
    // Count the remainders of elements in a
    for (int num : a) {
        int rem = num % m;
        remainder_count[rem]++;
    }
    
    long long ans = 0;
    
    // Check for each element in b
    for (int num : b) {
        int rem = num % m;
        int required_rem = (m - rem) % m;
        if (remainder_count.find(required_rem) != remainder_count.end()) {
            ans += remainder_count[required_rem];
        }
    }
    
    return ans;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    
    int t;
    cin >> t;
    
    while (t--) {
        int n, m;
        cin >> n >> m;
        
        vector<int> a(n), b(n);
        for (int i = 0; i < n; ++i) {
            cin >> a[i];
        }
        for (int i = 0; i < n; ++i) {
            cin >> b[i];
        }
        
        cout << solve(a, b, m) << "\n";
    }
    
    return 0;
}
