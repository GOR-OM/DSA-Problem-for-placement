vector<int> getOrder(vector<vector<int>>& tasks) {
    vector<pair<pair<int,int>,int>> all;
    for (int i = 0; i < tasks.size(); ++i) {
        all.push_back({{tasks[i][0], tasks[i][1]}, i});
    }
    sort(all.begin(), all.end());
    
    vector<int> ans;
    priority_queue<pair<int,int>, vector<pair<int,int>>, greater<pair<int,int>>> pq;
    
    long long currentTime = 0; 
    int taskIndex = 0;
    int n = tasks.size();

    while (taskIndex < n || !pq.empty()) {
        if (pq.empty()) {
            currentTime = max(currentTime, (long long)all[taskIndex].first.first);
        }
        
        while (taskIndex < n && all[taskIndex].first.first <= currentTime) {
            pq.push({all[taskIndex].first.second, all[taskIndex].second});
            ++taskIndex;
        }
        
        
        auto it = pq.top();
        pq.pop();
        currentTime += it.first;
        ans.push_back(it.second);
    }
    
    return ans;
}











