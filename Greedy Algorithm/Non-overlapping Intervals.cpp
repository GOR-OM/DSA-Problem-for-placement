static bool config(pair<pair<int, int>, int> a1, pair<pair<int, int>, int> a2)
{
    if (a1.first.second == a2.first.second)
        return a1.first.first > a2.first.first;
    return a1.first.second < a2.first.second;
}

int eraseOverlapIntervals(vector<vector<int>> &pairs)
{
    vector<pair<pair<int, int>, int>> arr;
    for (int i = 0; i < pairs.size(); i++)
    {
        arr.push_back({{pairs[i][0], pairs[i][1]}, i});
    }
    sort(arr.begin(), arr.end(), config);
    int ans = 0;
    int time = 0;
    for (auto it : arr)
    {
        if (it.first.first >= time)
        {
            time = it.first.second;
            ans++;
        }
    }
    int n = pairs.size();
    return n - ans;
}

