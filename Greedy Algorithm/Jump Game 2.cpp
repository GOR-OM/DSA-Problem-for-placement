int jump(vector<int>& arr) {
    int jumps = 0;
    int l = 0;
    int r = 0;
    int n = arr.size();
    
    if (n == 1) return 0;
    while (r < n - 1) {
        int k = r + 1;
        int y = r;
        for (int i = l; i <= y; i++) {
            r = max(r, i + arr[i]);
           
        }
        if (k > r) return -1; 
        l = k;
        
        jumps++;
    }
    return jumps;
}

