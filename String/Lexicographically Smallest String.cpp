string getSmallestString(string s, int k) {
    string ans = "";
    for(int i=0;i<s.size();i++){
        int left = s[i] - 'a';
        int right = 'z' - s[i] +1 ;
        int minCost = min(left,right);
        if(minCost<=k){
            k-=minCost;
            ans+='a';
        }
        else {
            char c  = s[i] - k;
            k=0;
            ans+=c;
        }
    }
    return ans;
}








