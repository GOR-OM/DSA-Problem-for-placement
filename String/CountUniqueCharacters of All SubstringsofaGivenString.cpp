int uniqueLetterString(string s) {
    map<char,vector<int>>alls;
    for(int i=0;i<s.size();i++){
        alls[s[i]].push_back(i);
    }
    long long ans=0;
    for(auto it : alls){
        if(it.second.size()==1){
            int leftWindow  = it.second[0]+1 ;
            int rightWindow = s.size() - it.second[0]  ;
            ans+=(leftWindow*rightWindow);
            // cout<<it.first<<" "<<it.second[0]<<" "<<leftWindow<<" "<<rightWindow<<endl;
        }
        else {
            int k = it.second.size();
            for(int i=0;i<k;i++){
                if(i==0){
                    int leftWindow = it.second[i]+1;
                    int rightWindow = it.second[i+1] - it.second[i]  ;
                    ans+= (leftWindow*rightWindow);
                    // cout<<it.first<<" "<<it.second[i]<<" "<<leftWindow<<" "<<rightWindow<<endl;
                }
                else if (i==k-1){
                    // cout<<" k : "<<k<<endl;
                    int lefWindow = it.second[i] -it.second[i-1]   ; 
                    int rightWindow  = s.size() - it.second[i] ; 
                    ans+=(lefWindow * rightWindow);
                    // cout<<it.first<<" "<<it.second[i]<<" "<<lefWindow<<" "<<rightWindow<<endl;
                }
                else {
                    int l = it.second[i] -it.second[i-1]  ; 
                    int r = it.second[i+1] - it.second[i]  ;
                    ans +=(l*r);
                    // cout<<it.first<<" "<<it.second[i]<<" "<<l<<" "<<l<<endl;
                }
            }
        }
    }
    return ans ;
    
}










