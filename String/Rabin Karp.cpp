int strStr(string haystack, string needle) {
        int n = haystack.length();
        int m = needle.length();
        if (m > n) return -1;
        
        // Base and modulus for the hashing
        int base = 256; 
        int mod1 = 1e9 + 7;
        int mod2 = 1e9 + 9;
        
        long long needleHash1 = 0, needleHash2 = 0;
        long long haystackHash1 = 0, haystackHash2 = 0;
        long long basePow1 = 1, basePow2 = 1; // base^m-1 % mod
        
        // Precompute base^(m-1) % mod
        for (int i = 0; i < m - 1; i++) {
            basePow1 = (basePow1 * base) % mod1;
            basePow2 = (basePow2 * base) % mod2;
        }
        
        // Compute hash for needle and first window of haystack
        for (int i = 0; i < m; i++) {
            needleHash1 = (needleHash1 * base + needle[i]) % mod1;
            needleHash2 = (needleHash2 * base + needle[i]) % mod2;
            haystackHash1 = (haystackHash1 * base + haystack[i]) % mod1;
            haystackHash2 = (haystackHash2 * base + haystack[i]) % mod2;
        }
        
        // Slide the window over haystack
        for (int i = 0; i <= n - m; i++) {
            // Check if the hashes match and if so, check the strings to confirm
            if (needleHash1 == haystackHash1 && needleHash2 == haystackHash2) {
                if (haystack.substr(i, m) == needle) {
                    return i;
                }
            }
            // Compute the hash of the next window
            if (i < n - m) {
                haystackHash1 = (base * (haystackHash1 - haystack[i] * basePow1) + haystack[i + m]) % mod1;
                if (haystackHash1 < 0) haystackHash1 += mod1;
                
                haystackHash2 = (base * (haystackHash2 - haystack[i] * basePow2) + haystack[i + m]) % mod2;
                if (haystackHash2 < 0) haystackHash2 += mod2;
            }
        }
        
        return -1;
    }











