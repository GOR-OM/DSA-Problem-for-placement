vector<int> KMP(string s)
{
    vector<int> lps(s.size(), 0);
    for (int i = 1; i < s.size(); i++)
    {
        int prev_index = lps[i - 1];

        while (prev_index > 0 && s[i] != s[prev_index])
        {
            prev_index = lps[prev_index - 1];
        }
        lps[i] = prev_index + (s[i] == s[prev_index] ? 1 : 0);
    }
    return lps;
}

vector<int> beautifulIndices(string s, string a, string b, int k)
{
    string Sa = a + "#" + s;
    string Sb = b + "#" + s;
    vector<int> sa = KMP(Sa);
    vector<int> sb = KMP(Sb);

    vector<int> as;
    for (int i = a.size(); i < Sa.size(); i++)
    {
        if (sa[i] == a.size())
        {
            if (i - 2 * a.size() >= 0)
            {
                as.push_back(i - 2 * a.size());
            }
        }
    }
    vector<int> bs;
    for (int i = b.size(); i < Sb.size(); i++)
    {
        if (sb[i] == b.size())
        {
            if (i - 2 * b.size() >= 0)
            {
                bs.push_back(i - 2 * b.size());
            }
        }
    }
    vector<int> ans;
    if (as.size() == 0 || bs.size() == 0)
    {
        return ans;
    }
    for (int i = 0; i < as.size(); i++)
    {
        auto bi = lower_bound(bs.begin(), bs.end(), as[i]) - bs.begin();
        if (bi != bs.size() && abs(bs[bi] - as[i]) <= k)
        {
            ans.push_back(as[i]);
            continue;
        }
        if (bi != 0 && abs(bs[bi - 1] - as[i]) <= k)
            ans.push_back(as[i]);
    }
    return ans;
}



