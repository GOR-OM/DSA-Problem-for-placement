vector<int>KMP (string s){
    int n= s.size();
    vector<int>lps(n,0);
    for(int i=1;i<n;i++){
        int pre_index = lps[i-1];

        while(pre_index>0 && s[i]!=s[pre_index]){
            pre_index = lps[pre_index-1];
        }
        lps[i] = pre_index + (s[i]==s[pre_index] ? 1 : 0);
        return lps;
    }
}



int countMatchingSubarrays(vector<int>& nums, vector<int>& pattern) {
    string patterns = "";
    for(int i=0;i<pattern.size();i++){
        if(pattern[i]==0)patterns+='s';
        else if(pattern[i]==-1)patterns+='d';
        else patterns+='a';
    }
    string s = "";
    for(int i=0;i<nums.size()-1;i++){
        if(nums[i+1] - nums[i]==0)s+='s';
        if(nums[i+1] - nums[i]==1)s+='a';
        if(nums[i+1] - nums[i]==-1)s+='d';
    }

    string S = patterns + '#' + s ;
    vector<int> lps = KMP(S);
    int ans=0;
    for(int i=0;i<lps.size();i++){
        if(lps[i]==patterns.size())ans++;
    }
    return ans;


}








