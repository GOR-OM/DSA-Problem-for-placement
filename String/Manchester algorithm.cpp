vector<int> Manchester(string s)
{
    int n = s.length();
    vector<int> lps(n, 0);
    int center = 0;
    int rightMost = 0;
    for (int i = 2; i < s.length() - 1; i++)
    {
        int mirror = center - (i - center);
        if (i < rightMost)
        {
            lps[i] = min(lps[mirror], rightMost - i);
        }

        while (s[i + lps[i] + 1] == s[i - 1 - lps[i]])
        {
            lps[i]++;
        }

        if (i + lps[i] > rightMost)
        {
            center = i;
            rightMost = i + lps[i];
        }
    }
    return lps;
}

string longestPalindrome(string s)
{
    string newS = "@";
    for (int i = 0; i < s.length(); i++)
    {
        newS += '#';
        newS += s[i];
    }
    newS += "#$";
    vector<int> lps = Manchester(newS);
    int maxi = 0;
    int index = 0;
    for (int i = 2; i < lps.size(); i++)
    {
        if (lps[i] >= maxi)
        {
            maxi = lps[i];
            index = i;
        }
    }

    index = (index - 2) / 2;
    index = index - maxi / 2;
    if (maxi % 2 == 0)
        index++;
    string ans = "";
    for (int i = index; i < index + maxi; i++)
    {
        ans += s[i];
    }
    return ans;
}

