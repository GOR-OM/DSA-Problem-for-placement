int maxScore(vector<int>& cardPoints, int k) {
      int n = cardPoints.size();
   if(k==0)return 0;
   long long tsum=0;
   for(int i =0;i<n;i++){
        tsum+=cardPoints[i];
   }
   if(k==n)return tsum;
   int windo = n-k;
   int mini = INT_MAX;
   int sum2=0;
   for(int i=0;i<windo;i++ ){
      
        sum2+=cardPoints[i];
   }
   mini = min(mini,sum2);
   for(int i=windo;i<n;i++){
        sum2 = sum2 - cardPoints[i-windo]+ cardPoints[i]; 
        mini = min(mini,sum2);
   }
   return tsum - mini ;
}


