int maxScore(vector<int>& arr , int k){
    long long sum=0;
    for(int i=0;i<k;i++){
        sum+=arr[i];
    }
    long long maxi = sum;
    int right = arr.size()-1;
    for(int i=k-1;i>=0;i--){
        sum-=arr[i];
        sum+=arr[right];
        right--;
        maxi= max(maxi,sum);
    }
    return maxi;

}








