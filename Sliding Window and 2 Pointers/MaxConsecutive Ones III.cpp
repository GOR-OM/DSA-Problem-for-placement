void parse_input_and_solve(std::ofstream& out, std::string& s, std::string& t) {
    int n = s.size()-1;
    int k = stoi(t);
    int flip = 0, l = 1, r = 1, len = 0;
    while(r < n) {
        if(s[r] == 48) flip++;
        if(flip > k) {
            if(s[l] == 48) flip--;
            l += 2;
        }
        len = max(len, r/2-l/2+1);
        r += 2;
    }
    out << len << endl;
}

