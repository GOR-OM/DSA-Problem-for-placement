int totalFruit(vector<int> &s)
{
    int n = s.size();
    int left = 0;
    int right = 0;
    unordered_map<int, int> countMap;

    int maxLength = 0;

    while (right < n)
    {
        countMap[s[right]]++;
        right++;

        if (countMap.size() == 2)
        {
            maxLength = max(maxLength, right - left);
        }

        while (countMap.size() > 2)
        {
            countMap[s[left]]--;
            if (countMap[s[left]] == 0)
            {
                countMap.erase(s[left]);
            }
            left++;
        }
    }

    if (maxLength == 0)
        return countMap[s[0]];
    return maxLength;
}



