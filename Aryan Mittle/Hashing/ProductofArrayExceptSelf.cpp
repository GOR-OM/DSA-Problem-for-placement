vector<int> productExceptSelf(vector<int>& nums) {
    long long int mul=1;
    bool zero=false;
    int cnt=0;
    for(int i=0;i<nums.size();i++){
        if(nums[i]==0){
            zero=true;
            cnt++;
            continue;
        }
        mul*=nums[i];
    }
    vector<int>ans(nums.size(),0);
    if(cnt>1){
        return ans;
    }
    for(int i=0;i<nums.size();i++){
        if(zero && nums[i]==0 ){
            ans[i]=mul;
            continue;
        }
        if(zero){
            ans[i]=0;
        }
        else {
            ans[i]= mul/nums[i];
        }
        
    }
    return ans;

    
}

vector<int> productExceptSelf(vector<int>& nums) {
    int n = nums.size();
    vector<int> answer(n, 1);

    // Create prefix product array
    int prefix = 1;
    for (int i = 0; i < n; ++i) {
        answer[i] = prefix;
        prefix *= nums[i];
    }

    // Create postfix product and multiply with the prefix product
    int postfix = 1;
    for (int i = n - 1; i >= 0; --i) {
        answer[i] *= postfix;
        postfix *= nums[i];
    }

    return answer;
}






