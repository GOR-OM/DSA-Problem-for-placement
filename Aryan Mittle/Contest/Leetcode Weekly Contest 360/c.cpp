int minOperations(vector<int>& nums, int target) {
    int n= nums.size();
    long long sum=0;
    vector<int >c(31,0);
    for(int i=0;i<n;i++){
        int x = nums[i];
        for(int j=0;j<=30;j++){
            if(x&(1<<j)){
                c[j]++;
            }
        }
        sum+=x;
    }  
    if(target>sum)return -1 ;
    int i=0;
    int res= 0;
    while(i<30){
        if((1<<i)&target){
            if(c[i]>0){
                c[i]--;
            }
            else {
                int j=i;
                while(j<30 && c[j]==0){
                    j++;
                    res++;
                }
                c[j]--;
                while(j>i){
                    c[--j]++;
                }
            }
        }
        c[i+1] += c[i]/2;
        i++;
    }
    return res;
}





