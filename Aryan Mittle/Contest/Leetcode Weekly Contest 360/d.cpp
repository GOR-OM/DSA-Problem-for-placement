  long long solve(int node, long long par, long long k, vector<vector<pair<int, long long>>>& dp) {
        if (par < 0) {
            return 0;
        }
        if ((k & ((long long)1 << par)) == 0) {
            return solve(node, par - 1, k, dp);
        }
        return dp[par][node].second + solve(dp[par][node].first, par - 1, k, dp);
    }

    long long getMaxFunctionValue(vector<int>& receiver, long long k) {
        int m = 40;
        int n = receiver.size();
        // dp[i][j] where i moves in 2^i steps
        vector<vector<pair<int, long long>>> dp(m, vector<pair<int, long long>>(n));

        // Initialize dp for step 2^0
        for (int i = 0; i < n; i++) {
            dp[0][i] = {receiver[i], receiver[i]};
        }

        // Fill the DP table for steps 2^i
        for (int i = 1; i < m; i++) {
            for (int j = 0; j < n; j++) {
                int x = dp[i - 1][j].first;
                dp[i][j] = {dp[i - 1][x].first, dp[i - 1][x].second + dp[i - 1][j].second};
            }
        }

        long long res = 0;
        // Calculate the maximum possible score
        for (int i = 0; i < n; i++) {
            res = max(solve(i, m - 1, k, dp) + i, res);
        }
        return res;
    }





