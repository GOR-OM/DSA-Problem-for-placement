int maxSumTriplet(vector<int>& A) {
    int n = A.size();
    if (n < 3) return 0;

    // Step 1: Create right maximum array
    vector<int> rightMax(n);
    rightMax[n - 1] = A[n - 1];
    for (int i = n - 2; i >= 0; --i) {
        rightMax[i] = max(rightMax[i + 1], A[i]);
    }

    // Step 2: Traverse the array and use a set to find left maximums
    set<int> leftMaxSet;
    leftMaxSet.insert(A[0]);
    int maxSum = 0;
    for (int j = 1; j < n - 1; ++j) {
        if (rightMax[j + 1] > A[j]) {
            auto it = leftMaxSet.lower_bound(A[j]);
            if (it != leftMaxSet.begin()) {
                --it;
                int leftMax = *it;
                maxSum = max(maxSum, leftMax + A[j] + rightMax[j + 1]);
            }
        }
        leftMaxSet.insert(A[j]);
    }

    return maxSum;
}










