int solve(vector<int> &A, int B) {
    int n = A.size();
    long long sum = 0;
    long long curr = 0;

    // Calculate initial sum of the first B elements
    for (int i = 0; i < B; i++) {
        curr += A[i];
    }
    sum = curr;

    // Slide the window from the end to the start
    for (int i = 0; i < B; i++) {
        curr -= A[B - i - 1];    // Remove the element from the start of the window
        curr += A[n - i - 1];    // Add the element from the end of the array
        sum = max(sum, curr);    // Update the maximum sum found
    }

    return sum;
}







