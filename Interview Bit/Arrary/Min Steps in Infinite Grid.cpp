int coverPoints(vector<int> &a, vector<int> &b) {
    int aa = a[0];
    int bb =b[0];
    long long ans=0;
    for(int i=1;i<a.size();i++){
        int diff1 = abs(a[i]-aa);
        int diff2 = abs(b[i]-bb);
        ans+=max(diff1,diff2);
    }
    return ans;
}







