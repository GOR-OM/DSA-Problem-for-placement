int maxSubArray(const vector<int> &A) {
    int  ans =0;
    int sum=0;
    for(int i=0;i<A.size();i++){
        sum+=A[i];
        if(sum<0){
            sum=0;
        }
        ans = max(ans,sum);
    }
    return ans;
}










