int solve(int i, int prev, vector<int>& arr, vector<vector<int>>& dp) {
    if (i == arr.size()) {
        return 0;
    }
    if (dp[i][prev + 1] != -1) return dp[i][prev + 1];

    int notTake = solve(i + 1, prev, arr, dp);
    int take = 0;
    if (prev == -1 || arr[i] % arr[prev] == 0) {
        take = 1 + solve(i + 1, i, arr, dp);
    }
    return dp[i][prev + 1] = max(notTake, take);
}

int largestDivisibleSubset(vector<int>& arr) {
    SORT(arr);
    int n = arr.size();
    vector<vector<int>> dp(n + 1, vector<int>(n + 1, -1));
    return solve(0, -1, arr, dp);
}








