vector<vector<int>> dp(2000 + 1, vector<int>(2000 + 1, 0));
bool isMatch(string w1, string w2)
{
    int n = w1.size();
    int m = w2.size();

    dp[0][0] = 1;

    for (int j = 1; j <= m; ++j)
    {
        if (w2[j - 1] == '*')
        {
            dp[0][j] = dp[0][j - 1];
        }
    }

    for (int i = 1; i <= n; ++i)
    {
        for (int j = 1; j <= m; ++j)
        {
            if (w2[j - 1] == w1[i - 1] || w2[j - 1] == '?')
            {
                dp[i][j] = dp[i - 1][j - 1];
            }
            else if (w2[j - 1] == '*')
            {
                dp[i][j] = dp[i][j - 1] || dp[i - 1][j];
            }
        }
    }

    return dp[n][m];
}

