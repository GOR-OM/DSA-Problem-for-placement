vector<int> findPatterns(const string& s) {
    vector<int> result;
    int n = s.length();
    
    for (int i = 0; i < n; ) {
        int start = i;
        bool found = false;

        while (i + 2 < n && (s.substr(i, 3) == "ABA" || s.substr(i, 3) == "BAB")) {
            found = true;
            i += 2;
        }

        if (found) {
            result.push_back(i - start + 1);
            i++; // Move to the next character after the current sequence
        } else {
            i++;
        }
    }
    
    return result;
}





