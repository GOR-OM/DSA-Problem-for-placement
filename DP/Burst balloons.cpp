int maxCoins(vector<int> &arr)
{
    int n = arr.size();
    arr.push_back(1);
    arr.insert(arr.begin(), 1);
    vector<vector<int>> dp(n + 2, vector<int>(n + 2, 0));

    for (int i = n; i >= 1; i--)
    {
        for (int j = i; j <= n; j++)
        {
            int maxi = INT_MIN;
            for (int k = i; k <= j; k++)
            {
                int val = dp[i][k - 1] + dp[k + 1][j] + arr[i - 1] * arr[k] * arr[j + 1];
                maxi = max(maxi, val);
            }
            dp[i][j] = maxi;
        }
    }
    return dp[1][n];
}

