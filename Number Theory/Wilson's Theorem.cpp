long long  power(long long  x, long long  y, long long p)
{
    long long  res = 1;
    x = x % p;
    if (x == 0) return 0;
    while (y > 0)
    {
        if (y & 1)
            res = (res * x) % p;
        y = y >> 1;
        x = (x * x) % p;
    }
    return res;
}


long long  solve(long long  n ,long long  p){
    if(n>=p)return 0;

    long long  ans = p-1 ;
    for(long long  i=n+1;i<=p-1;i++){
        ans = (ans * power(i,p-2,p))%p;
    }
    return ans;
}


