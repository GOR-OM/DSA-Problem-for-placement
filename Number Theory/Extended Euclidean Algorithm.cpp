tuple<int, int, int> extendedGCD(int a, int b) {
    if (b == 0) {
        return {a, 1, 0}; // Base case: gcd(a, 0) = a and x = 1, y = 0
    } else {
        // Recursive case
        int gcd, x1, y1;
        tie(gcd, x1, y1) = extendedGCD(b, a % b);
        
        // Update x and y using the results of recursion
        int x = y1;
        int y = x1 - (a / b) * y1;
        
        return {gcd, x, y};
    }
}

int main() {
    int a, b;
    cout << "Enter values for a and b: ";
    cin >> a >> b;

    int gcd, x, y;
    tie(gcd, x, y) = extendedGCD(a, b);

    cout << "gcd(" << a << ", " << b << ") = " << gcd << "\n";
    cout << "x = " << x << ", y = " << y << "\n";
    cout << a << " * " << x << " + " << b << " * " << y << " = " << gcd << "\n";

    return 0;
}